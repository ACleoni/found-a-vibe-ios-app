import SwiftUI

extension Font {
    public static func abeezee(size: CGFloat) -> Font {
        Font.custom("ABeeZee-Regular", size: size)
    }
    
    public static func abeezeeItalic(size: CGFloat) -> Font {
        Font.custom("ABeeZee-Italic", size: size)
    }
}
